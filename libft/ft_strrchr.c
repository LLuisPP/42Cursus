char	*ft_strrchr(const char *s, int c)
{
	int	l;

	l = 0;
	while (s[l] != '\0')
		l++;
	while (l >= 0)
	{
		if (s[l] == (unsigned char) c)
		{
			return ((char *) &s[l]);
		}
		l--;
	}
	return ((char *) 0);
}
