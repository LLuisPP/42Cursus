#include "ft_printf.h"

void	ft_putstr(char *s)
{
	int	len;
	int	i;

	i = 0;
	if (s)
	{
		while (s[i] != 0)

			i++;
		len = i;
		i = 0;
		while (s[i] != 0)
		{
			ft_putchar(s[i]);
			i++;
		}
	}
}